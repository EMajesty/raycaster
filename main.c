void drawRays2D()
{
    glColor3f(0, 1, 1);
    glBegin(GL_QUADS);
    glVertex2i(526, 0);
    glVertex2i(1006, 0);
    glVertex2i(1006, 160);
    glVertex2i(526, 160);
    glEnd();

    glColor3f(0, 0, 1);
    glBegin(GL_QUADS);
    glVertex2i(526, 160);
    glVertex2i(1006, 160);
    glVertex2i(1006, 320);
    glVertex2i(526, 320);
    glEnd();

    int r, mx, my, mp, dof, side;
    float rx, ry, rayAngle, xo, yo, vx, vy, disV, disH;

    rayAngle = fixAng(playerAngle + 30);

    for(r = 0; r < 60; r++)
    {
        int vmt = 0, hmt = 0;

        //---Check Vertical Lines---
        dof = 0;
        side = 0;
        disV = 100000;
        float rayTan = tan(degToRad(rayAngle));

        // looking left
        if(cos(degToRad(rayAngle)) > 0.001)
        {
            rx = (((int)playerX>>6)<<6)+64;
            ry = (playerX - rx) * rayTan + playerY;
            xo = 64;
            yo = -xo * rayTan;
        }

        // looking right
        else if(cos(degToRad(rayAngle)) < -0.001)
        {
            rx = (((int)playerX>>6)<<6) - 0.0001;
            ry = (playerX - rx) * rayTan + playerY;
            xo = -64;
            yo = -xo * rayTan;
        } else
        {
            rx = playerX;
            ry = playerY;
            dof = 8;
        }

        while(dof < 8)
        {
            mx = (int)(rx)>>6;
            my = (int)(ry)>>6;
            mp = my * mapX + mx;

            // hit wall
            if(mp > 0 && mp < mapX * mapY && mapW[mp] > 0)
            {
                vmt = mapW[mp] - 1;
                dof = 8;
                disV = cos(degToRad(rayAngle)) * (rx - playerX) - sin(degToRad(rayAngle)) * (ry - playerY);
            } else
            {
                // check next vertical line
                rx += xo;
                ry += yo;
                dof += 1;
            }
        }

        vx = rx;
        vy = ry;

 
        //---Check Horizontal Lines---
        dof = 0;
        disH = 100000;
        rayTan = 1.0/rayTan;

        // looking up
        if(sin(degToRad(rayAngle)) > 0.001) 
        {
            ry = (((int)playerY>>6)<<6) - 0.0001;
            rx = (playerY - ry) * rayTan + playerX;
            yo = -64;
            xo = -yo * rayTan;
        }

        // looking down
        else if(sin(degToRad(rayAngle)) < -0.001) 
        {
            ry = (((int)playerY>>6)<<6)+64;
            rx = (playerY - ry) * rayTan + playerX;
            yo = 64;
            xo = -yo * rayTan;
        }

        // looking straight left or right
        else
        {
            rx = playerX;
            ry = playerY;
            dof = 8;
        }

        while(dof < 8)
        {
            mx = (int)(rx)>>6;
            my = (int)(ry)>>6;
            mp = my * mapX + mx;
            
            // hit wall
            if(mp > 0 && mp < mapX * mapY && mapW[mp] > 0)
            {
                hmt = mapW[mp] - 1;
                dof = 8;
                disH = cos(degToRad(rayAngle)) * (rx - playerX) - sin(degToRad(rayAngle)) * (ry - playerY);
            } else
            {
                // check next horizontal line
                rx += xo;
                ry += yo;
                dof += 1;
            }
        }
       
        float shade = 1;
        glColor3f(0, 0.8, 0);

        // horizontal hit first
        if(disV < disH)
        {
            hmt = vmt;
            shade = 0.5;
            rx = vx;
            ry = vy;
            disH = disV;
            glColor3f(0, 0.6, 0);
        }

        // draw 2D ray
        glLineWidth(2);
        glBegin(GL_LINES);
        glVertex2i(playerX, playerY);
        glVertex2i(rx, ry);
        glEnd();

        //---Draw 3D Walls---

        // fix fisheye
        int ca = fixAng(playerAngle - rayAngle);
        disH = disH * cos(degToRad(ca));

        int lineH = (mapS * 320) / (disH);
        float ty_step = 32.0 / (float)lineH;
        float ty_off = 0;

        if(lineH > 320)
        {
            ty_off = (lineH - 320) / 2.0;
            lineH = 320;
        }
        int lineOff = 160 - (lineH >> 1);

        int y;
        float ty = ty_off * ty_step + hmt * 32;
        float tx;

        if(shade == 1)
        {
            tx = (int)(rx / 2.0) % 32;
            if(rayAngle > 180)
            {
                tx = 31 - tx;
            }
        } else
        {
            tx = (int)(ry / 2.0) % 32;
            if(rayAngle > 90 && rayAngle < 270)
            {
                tx = 31 - tx;
            }
        }

        for(y = 0; y < lineH; y++)
        {
            float c = allTextures[(int)(ty) * 32 + (int)(tx)] * shade;
            if(hmt == 0){ glColor3f(c, c / 2.0, c / 2.0); } // checkerboard red
            if(hmt == 1){ glColor3f(c, c, c / 2.0); } // brick yellow
            if(hmt == 2){ glColor3f(c / 2.0, c / 2.0, c ); } // window blue
            if(hmt == 3){ glColor3f(c / 2.0, c, c / 2.0); } // door green

            // draw vertical wall
            glPointSize(8);
            glBegin(GL_POINTS);
            glVertex2i(r * 8 + 530, y + lineOff);
            glEnd();

            ty += ty_step;
        }

        // draw floors
        for(y = lineOff + lineH; y < 320; y++)
        {
            float dy = y - (320 / 2.0);
            float deg = degToRad(rayAngle);
            float raFix = cos(degToRad(fixAng(playerAngle - rayAngle)));

            tx = playerX / 2 + cos(deg) * 158 * 32 / dy / raFix;
            ty = playerY / 2 - sin(deg) * 158 * 32 / dy / raFix;

            int mp = mapF[(int)(ty / 32.0) * mapX + (int)(tx / 32.0)] * 32 * 32;
            float c = allTextures[((int)(ty) & 31) * 32 + ((int)(tx) & 31) + mp] * 0.7;
            glColor3f(c / 1.3, c / 1.3, c);
            glPointSize(8);
            glBegin(GL_POINTS);
            glVertex2i(r * 8 + 530, y);
            glEnd();

            // draw ceiling
            mp = mapC[(int)(ty / 32.0) * mapX + (int)(tx / 32.0)] * 32 * 32;
            c = allTextures[((int)(ty) & 31) * 32 + ((int)(tx) & 31) + mp] * 0.7;
            glColor3f(c / 2.0, c / 1.2, c / 2.0);
            glPointSize(8);
            glBegin(GL_POINTS);
            glVertex2i(r * 8 + 530, 320 - y);
            glEnd();
        }

        // go to next ray, 60 total
        rayAngle = fixAng(rayAngle - 1);
    }
}

void display()
{
    // flames per second
    frame2 = glutGet(GLUT_ELAPSED_TIME);
    fps = (frame2 - frame1);
    frame1 = glutGet(GLUT_ELAPSED_TIME);
    
    // buttons
    if(Keys.a==1)
    {
        playerAngle += 0.2 * fps;
        playerAngle = fixAng(playerAngle);
        playerDeltaX = cos(degToRad(playerAngle));
        playerDeltaY = -sin(degToRad(playerAngle));
    }

    if(Keys.d==1)
    {
        playerAngle -= 0.2 * fps;
        playerAngle = fixAng(playerAngle);
        playerDeltaX = cos(degToRad(playerAngle));
        playerDeltaY = -sin(degToRad(playerAngle));
    }

    // x offset to check map
    int xOffset = 0;
    if(playerDeltaX < 0)
    {
        xOffset = -20;
    } else
    {
        xOffset = 20;
    }

    // y offset to check map
    int yOffset = 0;
    if(playerDeltaY < 0)
    {
        yOffset = -20;
    } else
    {
        yOffset = 20;
    }

    // x position and offset
    int iPosX = playerX / 64.0;
    int iPosXAddXOffset = (playerX + xOffset) / 64.0;
    int iPosXSubXOffset = (playerX + xOffset) / 64.0;

    // y position and offset
    int iPosY = playerY / 64.0;
    int iPosYAddYOffset = (playerY + yOffset) / 64.0;
    int iPosYSubYOffset = (playerY - yOffset) / 64.0;

    if(Keys.w==1)
    {
        if(mapW[iPosY * mapX + iPosXAddXOffset] == 0)
        {
            playerX += playerDeltaX * 0.2 * fps;
        }
        if(mapW[iPosYAddYOffset * mapX + iPosX] == 0)
        {
            playerY += playerDeltaY * 0.2 * fps;
        }
    }

    if(Keys.s==1)
    {
        if(mapW[iPosY * mapX + iPosXSubXOffset] == 0)
        {
            playerX -= playerDeltaX * 0.2 * fps;
        }
        if(mapW[iPosYSubYOffset * mapX + iPosX] == 0)
        {
            playerY -= playerDeltaY * 0.2 * fps;
        }
    }

    glutPostRedisplay();

    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    drawMap2D();
    drawPlayer2D();
    drawRays2D();
    glutSwapBuffers();
}

void ButtonDown(unsigned char key, int x, int y)
{
    if(key=='a'){ Keys.a=1;}
    if(key=='d'){ Keys.d=1;}
    if(key=='w'){ Keys.w=1;}
    if(key=='s'){ Keys.s=1;}
    if(key=='e')
    {
        int xOffset = 0;
        if(playerDeltaX < 0){ xOffset = -25; } else { xOffset = 25; }
        
        int yOffset = 0;
        if(playerDeltaY < 0){ yOffset = -25; } else { yOffset = 25; }

        int ipx = playerX / 64.0;
        int ipx_add_xo = (playerX + xOffset) / 64.0;
        int ipy = playerY / 64.0;
        int ipy_add_yo = (playerY + yOffset) / 64.0;

        if(mapW[ipy_add_yo * mapX + ipx_add_xo] == 4)
        {
            mapW[ipy_add_yo * mapX + ipx_add_xo] = 0;
        }
    }
    glutPostRedisplay();
}

void ButtonUp(unsigned char key, int x, int y)
{
    if(key=='a'){ Keys.a=0;}
    if(key=='d'){ Keys.d=0;}
    if(key=='w'){ Keys.w=0;}
    if(key=='s'){ Keys.s=0;}
    glutPostRedisplay();
}

